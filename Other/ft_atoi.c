#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int result;
	int negative;

	i = 0;
	result = 0;
	negative = 0;
	while ((str[i] == ' ') || (str[i] == '\t') || (str[i] == '\n')
		|| (str[i] == '\v') || (str[i] == '\r') || (str[i] == '\f'))
		i++;
	if (str[i] == '-')
		negative = 1;
	if ((str[i] == '-') || (str[i] == '+'))
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		result *= 10;
		result += str[i] - '0';
		i++;
	}
	if (negative == 1)
		return (-result);
	else
		return (result);
}
