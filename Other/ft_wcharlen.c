#include "libft.h"

size_t	ft_wcharlen(wchar_t c)
{
	unsigned int	v;
	int				size;

	v = (unsigned int)c;
	size = count_bits(c);
	if (size <= 7)
		return (1);
	else if (size <= 11)
		return (2);
	else if (size <= 16)
		return (3);
	else
		return (4);
}
